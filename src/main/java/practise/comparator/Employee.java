package practise.comparator;

public class Employee {
    private int salary;
    private String name;

    public void setSalary(int salary){
        this.salary = salary;
    }
    public void setName(String name){
        this.name = name;
    }

    public String getName(){
        return name;
    }
    public int getSalary(){
        return salary;
    }
}
